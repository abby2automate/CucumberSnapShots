package com.test.stepdefinition;

public class POJO {
	
	private String data1;
	private String data2;
	private int data3;
	private int data4;
	
	
	public POJO(String data1, String data2, int data3, int data4) {
		super();
		this.data1 = data1;
		this.data2 = data2;
		this.data3 = data3;
		this.data4 = data4;
	}


	public String getData1() {
		return data1;
	}


	public String getData2() {
		return data2;
	}


	public int getData3() {
		return data3;
	}


	public int getData4() {
		return data4;
	}
		
}
